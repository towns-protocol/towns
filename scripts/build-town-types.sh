#!/bin/bash
CHAIN="${1:-localhost}"
FROZEN="${2:-}"
ABI_DIR="packages/generated/${CHAIN}/v3/abis"

DENDRITE_DIR="servers/dendrite/towns/contracts"
DENDRITE_TOWNS_ARCHITECT_DIR="${DENDRITE_DIR}/${CHAIN}_towns_architect"
DENDRITE_TOWNS_ENTITLEMENTS_DIR="${DENDRITE_DIR}/${CHAIN}_towns_entitlements"

forge clean
forge build --extra-output-files metadata --extra-output-files abi

yarn typechain --target=ethers-v5 "packages/contracts/out/**/?(IDiamond|IDiamondCut|ITownArchitect|IProxyManager|IPausable|IEntitlements|IChannel|IRoles|ITokenEntitlement|IERC721|Permissions|TownFactory).json" --out-dir "packages/generated/${CHAIN}/v3/typings"

mkdir -p $ABI_DIR && cp -a packages/contracts/out/{Diamond,DiamondCut,TownArchitect,ProxyManager,Pausable,Entitlements,Channels,Roles,TokenEntitlement,ERC721,Permissions,TownFactory}.sol/* "$ABI_DIR"

# Copy the json abis to TS files for type inference
for file in $ABI_DIR/*.abi.json; do
  filename=$(basename  "$file" .json)
  echo "export default $(cat $file) as const" > $ABI_DIR/$filename.ts
done

mkdir -p $DENDRITE_DIR
mkdir -p $DENDRITE_TOWNS_ARCHITECT_DIR
mkdir -p $DENDRITE_TOWNS_ENTITLEMENTS_DIR

# Town Architect typings for Dendrite
go run github.com/ethereum/go-ethereum/cmd/abigen@v1.10.25 --abi packages/contracts/out/TownArchitect.sol/TownArchitect.abi.json --pkg "${CHAIN}_towns_architect" --type "${CHAIN}_towns_architect" --out "${DENDRITE_TOWNS_ARCHITECT_DIR}/${CHAIN}_towns_architect.go"

# Town Entitlements typings for Dendrite
go run github.com/ethereum/go-ethereum/cmd/abigen@v1.10.25 --abi packages/contracts/out/Entitlements.sol/Entitlements.abi.json --pkg "${CHAIN}_towns_entitlements" --type "${CHAIN}_towns_entitlements" --out "${DENDRITE_TOWNS_ENTITLEMENTS_DIR}/${CHAIN}_towns_entitlements.go"

# Using the $FROZEN flag and git diff, we can check if this script generates any new files
# under the $ABI_DIR or $DENDRITE_DIR directories.
if [ "$FROZEN" = "--frozen" ]; then
  if git diff --quiet --exit-code $ABI_DIR $DENDRITE_DIR; then
    echo "No new files generated by build-contract-types.sh"
  else
    echo "$(git diff -p $ABI_DIR $DENDRITE_DIR)"
    echo "Error: build-contract-types.sh generated new files with the --frozen flag. Please re-run ./scripts/build-contract-types.sh to re-generate the files and commit the changes."
    exit 1
  fi
fi
